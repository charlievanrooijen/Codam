#include <unistd.h>
#include <stdio.h>

int ft_recursive_power(int nb, int power)
{
    if(power > 1)
    {
        power--;
        nb *= ft_recursive_power(nb, power);
    }
    return nb;
}

int main()
{
    int res = ft_recursive_power(5, 5);
    printf("\n %d \n \n", res);
    return 0;
}
